# Napisz skrypt, w którym stworzysz operatory przyrostkowe dla operacji: +, -, *, /, **, %

a = 2.0
print("Stan początkowy: a = %.2f" % a)
a += 2
print("Po dodaniu 2 do a: a = %.2f" % a)
a -= 2
print("Po odjęciu 2 od a: a = %.2f" % a)
a *= 2
print("Po przemnożeniu a przez 2: a = %.2f" % a)
a /= 2
print("Po podzieleniu a przez 2: a = %.2f" % a)
a **= 2
print("Po podniesieniu a do kwadratu: a = %.2f" % a)
a %= 2
print("Po wykonaniu modulo przez 2 na a: a = %.2f" % a)
